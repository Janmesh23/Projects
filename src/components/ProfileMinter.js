export default function ProfileMinter() {
  const [username, setUsername] = useState('');
  const [profile, setProfile] = useState(null);
  const [error, setError] = useState('');

  const contractAddress = '0x9648D1a788Ec0e3b95D470952005325805320eA2'; // Get from Remix

  async function connectWallet() {
    if (!window.ethereum) {
      alert('Install MetaMask!');
      return;
    }
    await window.ethereum.request({ method: 'eth_requestAccounts' });
  }

  async function mintProfile() {
    try {
      if (!username) {
        setError("Username required");
        return;
      }
  
      const provider = new ethers.BrowserProvider(window.ethereum);
      const signer = await provider.getSigner();
      const contract = new ethers.Contract(
        contractAddress,
        [
          "function mintProfile(address player, string memory username) external",
          "function addressToTokenId(address) public view returns (uint256)",
          "function profiles(uint256) public view returns (string memory, uint256)",
        ],
        signer
      );
  
      const tx = await contract.mintProfile(await signer.getAddress(), username);
      await tx.wait();
      loadProfile();
    } catch (err) {
      setError(err.reason || err.message);
    }
  }

  async function loadProfile() {
    try {
      const provider = new ethers.BrowserProvider(window.ethereum);
      const signer = await provider.getSigner();
      const contract = new ethers.Contract(contractAddress, MetaverseProfileABI, provider);
      
      const tokenId = await contract.addressToTokenId(await signer.getAddress());
      if (tokenId > 0) {
        const profileData = await contract.profiles(tokenId);
        setProfile({
          username: profileData.username,
          joinDate: new Date(profileData.joinDate * 1000).toLocaleDateString()
        });
      }
    } catch (err) {
      setError(err.message);
    }
  }

  return (
    <div>
      <button onClick={connectWallet}>Connect Wallet</button>
      
      {!profile ? (
        <div>
          <input 
            value={username} 
            onChange={(e) => setUsername(e.target.value)}
            placeholder="Enter username"
          />
          <button onClick={mintProfile}>Create Profile</button>
        </div>
      ) : (
        <div>
          <h2>Profile</h2>
          <p>Username: {profile.username}</p>
          <p>Joined: {profile.joinDate}</p>
        </div>
      )}
      
      {error && <p style={{ color: 'red' }}>{error}</p>}
    </div>
  );
}